<script>
  function addtowishlist(product_id, variation_id = null) {
    $.ajax({
      url: '<?php echo SITE_URL; ?>ajaxcontroller.php',
      type: 'POST',
      data: {
        action: "wishlist",
        product_id: product_id,
        variation_id: variation_id
      },
      success: function(response) {
        let data = JSON.parse(response); // JSON को parse करें
        let wishlistIcon = $("#wishlist-icon-" + product_id);

        if (data.status === "success") {
          if (data.message === "Added to wishlist") {
            wishlistIcon.attr("src", "<?php echo SITE_URL ?>img/icon/heartfill.png");
          } else if (data.message === "Removed from wishlist") {
            wishlistIcon.attr("src", "<?php echo SITE_URL ?>img/icon/heart.png");
          } else {
            alert('Something went wrong.');
          }
        } else {
          alert('Something went wrong.');
        }
      },
      error: function() {
        alert('Something went wrong, please try again.');
      }
    });
  }
</script>

